package com.nepal.adversify.di.component;

import android.app.Application;

import com.generic.appbase.di.module.BaseAppModule;
import com.nepal.adversify.application.App;
import com.nepal.adversify.di.builder.ActivityBuilder;
import com.nepal.adversify.di.module.AppModule;
import com.nepal.adversify.ui.home.HomeProviderModule;

import javax.inject.Singleton;

import dagger.BindsInstance;
import dagger.Component;
import dagger.android.support.AndroidSupportInjectionModule;

@Singleton
@Component(modules = {
        AndroidSupportInjectionModule.class,
        BaseAppModule.class,
        AppModule.class,
        HomeProviderModule.class,
        ActivityBuilder.class})
public interface AppComponent {

    void inject(App app);

    @Component.Builder
    interface Builder {

        @BindsInstance
        Builder application(Application application);

        AppComponent build();
    }
}
